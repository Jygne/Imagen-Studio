"""
PSD Rename Worker — 通过 ExtendScript 驱动 Photoshop 批量重命名 PSD 图层

处理流程:
  1. 收集隐藏图层 ID（处理前快照）
  2. 解锁所有图层
  3. 删除隐藏/空白图层
  4. 应用图层蒙版
  5. 解散所有组（可选）
  6. 跳过无文本层 PSD（可选）
  7. 按类型重命名 + 栅格化
  8. 另存到输出目录

命名规则:
  TEXT                          → text / text1, text2 ...
  SHAPE/FILL + 覆盖 ≥80% 画布   → frame
  SHAPE/FILL（其余）             → name_shape / name_shape1 ...
  PIXEL / SMART OBJECT          → name_pixel（不编号）
"""
