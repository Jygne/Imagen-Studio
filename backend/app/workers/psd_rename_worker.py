# ── Photoshop 执行 ──────────────────────────────────────────────────────────

def _find_photoshop_app_name() -> str:
    """查找本机安装的 Photoshop 应用名。"""
    try:
        result = subprocess.run(
            ["mdfind", "kMDItemCFBundleIdentifier == 'com.adobe.Photoshop'"],
            capture_output=True, text=True, timeout=10,
        )
        for path in result.stdout.strip().splitlines():
            if path.endswith(".app"):
                name = os.path.basename(path).replace(".app", "")
                if "Photoshop" in name:
                    return name
    except Exception:
        pass
    return "Adobe Photoshop"


def run_photoshop_jsx(jsx_code: str, jsx_log_path: str) -> subprocess.Popen:
    """
    将 JSX 写入临时文件，用 osascript 驱动 Photoshop 执行。
    返回 Popen 句柄供调用方轮询。
    """
    jsx_path = os.path.join(tempfile.gettempdir(), "imagen_psd_rename.jsx")
    Path(jsx_path).write_text(jsx_code, encoding="utf-8")
    Path(jsx_log_path).write_text("", encoding="utf-8")  # clear log

    jsx_loader = (
        f"var _f=new File('{jsx_path}');"
        "_f.open('r');var _s=_f.read();_f.close();eval(_s);"
    )
    ps_app = _find_photoshop_app_name()
    logger.info("[psd_rename] Photoshop app: %s", ps_app)

    apple_script = (
        f'tell application "{ps_app}"\n'
        f'    activate\n'
        f'    delay 2\n'
        f'    do javascript "{jsx_loader}"\n'
        f'end tell'
    )
    as_path = os.path.join(tempfile.gettempdir(), "imagen_psd_rename.applescript")
    Path(as_path).write_text(apple_script, encoding="utf-8")

    process = subprocess.Popen(
        ["osascript", as_path],
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    logger.info("[psd_rename] osascript launched, pid=%s", process.pid)
    return process


# ── 日志解析 ────────────────────────────────────────────────────────────────

# Log line format (pipe-delimited):
#   opened|filename.psd
#   saved|filename.psd|/full/path.psd
#   skipped|filename.psd|reason
#   error|filename.psd|reason
#   rename|old_name|new_name|kind
#   started|N files
#   done|N files processed

_LOG_RE = re.compile(r"^(opened|saved|skipped|error|started|done)\|(.+)$")


def parse_jsx_log(log_path: str, last_offset: int = 0) -> tuple[list[dict], int]:
    """
    增量读取 JSX log 文件，返回 (事件列表, 新 offset)。

    事件 dict 结构:
        {"type": "saved"|"skipped"|"error"|"opened"|"done",
         "filename": "xxx.psd",
         "message": "..."}
    """
    events: list[dict] = []
    try:
        content = Path(log_path).read_text(encoding="utf-8", errors="replace")
    except FileNotFoundError:
        return events, last_offset

    new_lines = content[last_offset:]
    new_offset = last_offset + len(new_lines)

    for raw_line in new_lines.splitlines():
        line = raw_line.strip()
        if not line:
            continue
        m = _LOG_RE.match(line)
        if not m:
            continue
        event_type = m.group(1)
        rest = m.group(2).split("|")
        filename = rest[0] if rest else ""
        message = rest[1] if len(rest) > 1 else ""
        events.append({"type": event_type, "filename": filename, "message": message})

    return events, new_offset
